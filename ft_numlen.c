int	ft_numlen(int n)
{
	int	k;

	k = 0;
	if (n == '\0')
		return (1);
	if (n < 0)
	{
		k++;
		n *= -1;
	}
	while (n != 0)
	{
		k++;
		n = n / 10;
	}
	return (k);
}
